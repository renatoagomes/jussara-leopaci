    <p class="mt-5">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
    Por definição, é um termo referente ao nascimento que abrange os momentos imediatos antes, durante e após o parto. Porém, longe de acepções puramente biológicas, a psicologia insere um sentido mais amplo ao se apropriar do termo, que não diz apenas sobre o parto, mas também à gestação e ao puerpério – e é sobre essa perinatalidade que tratamos aqui. 
    </p>

    <br>

    <h3 class="mt-3">Psicologia Perinatal</h3>

    <p class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
    A psicologia perinatal é o “campo de estudo dos fenômenos psíquicos ligados ao ciclo gravídico-puerperal (...) que não se restringe ao evento imediato do parto, mas inclui as estapas que o antecedem e o ultrapassam relativas à gestação e ao puerpério – como a infertilidade, entrega do recém-nascido, abortos e óbitos de bebês – e seus efeitos sobre a relação entre os pais e sua prole” <a href="#abnt">¹</a>.
    </p>

    <br>

    <h3 class="mt-3">Psicoterapia Perinatal</h3>

    <p class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
    A gestação (desejada ou não), o parto e o pós-parto são experiências de intensidade e turbulência emocionais provocadas pelas mudanças físicas, afetivas e de identidade. São momentos de transição em que a mulher (e parceiro(a)) é inserida num universo a ser descoberto e permeado por muitos sentimentos, por vezes ambivalentes e de difícil compreensão. Tendo em vista a energia psíquica investida e a agitação emocional que esse período envolve, a psicoterapia perinatal oferece a possibilidade de adentrar nesse universo com o objetivo de ressignificar vivências e encontrar caminhos para um investimento amoroso e saudável na construção da mãe/pai em potencial.
    </p>
    

